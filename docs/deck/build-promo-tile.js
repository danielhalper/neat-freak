// Generates the 440x280 Chrome Web Store small promo tile.
// Output: store-promo-440x280.png in this folder.
// Matches the store screenshots: teal-dark hero + the real character.

const path = require("path");
const fs = require("fs");
const pptxgen = require("pptxgenjs");
const sharp = require("sharp");

const ROOT = path.resolve(__dirname, "..", "..");
const OUT_DIR = __dirname;
const OUT_FILE = path.join(OUT_DIR, "Neat-Freak-Promo-Tile.pptx");

// 440x280 at 96 DPI = 4.583" x 2.917"
const SLIDE_W = 4.583;
const SLIDE_H = 2.917;

const COLORS = {
  teal: "0F766E",
  tealDark: "093F3B",
  tealMid: "115E59",
  tealSoft: "D9F0E8",
  gold: "F4BD45",
  white: "FFFFFF"
};

async function svgFileToBase64Png(svgPath, width, height) {
  const svg = fs.readFileSync(svgPath);
  const pngBuffer = await sharp(svg, { density: 600 })
    .resize(width, height, { fit: "contain", background: { r: 0, g: 0, b: 0, alpha: 0 } })
    .png()
    .toBuffer();
  return "image/png;base64," + pngBuffer.toString("base64");
}

async function build() {
  const pres = new pptxgen();
  pres.defineLayout({ name: "PROMO_440x280", width: SLIDE_W, height: SLIDE_H });
  pres.layout = "PROMO_440x280";

  const logoPng = await svgFileToBase64Png(path.join(ROOT, "assets", "logo.svg"), 256, 256);
  // Real in-product character (generated by build-character-svgs.js). 1.6:1.
  const mascotPng = await svgFileToBase64Png(path.join(OUT_DIR, "mascot-character-happy.svg"), 800, 500);

  const s = pres.addSlide();
  s.background = { color: COLORS.tealDark };

  // Decorative circle that frames the character on the right, plus a faint
  // one bottom-left for balance (mirrors the hero + marquee treatment).
  s.addShape(pres.shapes.OVAL, {
    x: 2.7, y: 0.55, w: 2.15, h: 2.15,
    fill: { color: COLORS.tealMid, transparency: 50 },
    line: { color: COLORS.tealMid, transparency: 50 }
  });
  s.addShape(pres.shapes.OVAL, {
    x: -0.9, y: 2.0, w: 1.8, h: 1.8,
    fill: { color: COLORS.tealMid, transparency: 70 },
    line: { color: COLORS.tealMid, transparency: 70 }
  });

  // Wordmark only (top-left). The round character on the right is the single
  // "face" — repeating the app-icon face here would compete with it.
  s.addText("NEAT FREAK", {
    x: 0.3, y: 0.4, w: 3.0, h: 0.45,
    fontFace: "Calibri", fontSize: 15, bold: true, color: COLORS.gold, charSpacing: 9, margin: 0, valign: "middle"
  });

  // Headline (left, vertically centered, two lines — sized to fit cleanly)
  s.addText("Tidy your tabs.", {
    x: 0.3, y: 1.32, w: 2.55, h: 0.45,
    fontFace: "Georgia", fontSize: 20, bold: true, color: COLORS.white, margin: 0
  });
  s.addText("Find your work.", {
    x: 0.3, y: 1.74, w: 2.55, h: 0.45,
    fontFace: "Georgia", fontSize: 20, bold: true, italic: true, color: COLORS.gold, margin: 0
  });

  // Bottom accent + verb summary
  s.addShape(pres.shapes.RECTANGLE, {
    x: 0.3, y: 2.42, w: 0.42, h: 0.04,
    fill: { color: COLORS.gold }, line: { color: COLORS.gold }
  });
  s.addText("Tidy · Group · Find", {
    x: 0.3, y: 2.5, w: 2.6, h: 0.3,
    fontFace: "Calibri", fontSize: 10, color: COLORS.tealSoft, margin: 0
  });

  // The real character — fully visible, prominent, framed by the circle.
  // Kept within the 4.583" width so nothing crops off the right edge.
  s.addImage({ data: mascotPng, x: 2.62, y: 0.92, w: 1.95, h: 1.21875 });

  await pres.writeFile({ fileName: OUT_FILE });
  console.log("Wrote", OUT_FILE);
}

build().catch((err) => {
  console.error(err);
  process.exit(1);
});
